package functions;

public class EvenOdd {


    public static void main(String[] args) {
        
        
        int num =5;


        EvenOdd(num);


    }

    static void EvenOdd(int num)
    {
        if(num%2==0)
        {
            System.out.println("Thsi is even number");
        }
        else{
            System.out.println("This is odd number");
        }
    }
}
